# This program adds 3 numbers
print("Please input 3 different numbers: ")
num1 = int(input("Your first number please: "))
num2 = int(input("Your second number please: "))
num3 = int(input("Your third number please: "))

print('\n' + "The sum of number 1+2+3 is: " + str(num1 + num2 + num3))
print("The total of number 1-2-3 is: " + str(num1 - num2 - num3))
print("The multiple of number 1x2x3 is equal to: " + str(num1 * num2 * num3))
print("The Division of number 1/2/3 is equal to: " + str(num1 / num2 / num3))